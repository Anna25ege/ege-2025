
# Разбиение числа любой длины на разряды
num = 123
while num != 0: #не равно
    print(num % 10)
    num = num // 10

# num % 10 -> 3489 % 10 = 9
# num // 10 -> 3489 // 10 = 348

# num % 10 -> 348 % 10 = 8
# num // 10 -> 348 // 10 = 34

# num % 10 -> 34 % 10 = 4
# num // 10 -> 34 // 10 = 3

# num % 10 -> 3 % 10 = 3
# num // 10 -> 3 // 10 = 0